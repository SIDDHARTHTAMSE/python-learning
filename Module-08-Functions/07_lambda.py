# Lambda Functions

# Lambda means anonymous function.

# Normal Function:

def square(num):
    return num * num

print(square(5))


# Lambda Version:
# Example 1:

square = lambda num: num * num

print(square(5))

# Example 2:

add = lambda a, b: a + b

print(add(5, 6))


# Example 3

multiply = lambda a, b: a * b

print(multiply(5,4))