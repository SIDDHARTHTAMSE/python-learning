# 2. if-else Statement
# What is if-else?

# When we have two choices:
# If condition is True:
# execute if block
# If condition is False:
# execute else block

# Syntax:

# if condition:
#     statement
# else:
#     statement

# Example

age = 18

if age >= 18:
    print("Eligible for vote")
else:
    print("Not eligible")


# Check Even or Odd

number = 4

if number % 2 == 0:
    print("Number is even")
else:
    print("Number is odd")