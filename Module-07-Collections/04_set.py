# 3. Set
# What is Set?

# A Set stores unique values.

# Example:

numbers = {1,2,3,4}
print(numbers)

# Duplicate Removal
# Example:

numbers = {1,2,2,3,3,4}

print(numbers)

# Add Item
numbers.add(5)
print(numbers)

# Remove Item
numbers.remove(2)
print(numbers)


# Loop Through Set

for num in numbers:
    print(num)