# 1. List
# What is a List?

# A List stores multiple values in one variable.

# Example:

fruits = ["Apple", "Mango", "Banana"]

# Features of List

# ✅ Ordered

# ✅ Mutable (can be changed)

# ✅ Allows duplicates

# Example:

fruits = ["Apple", "Mango", "Apple"]