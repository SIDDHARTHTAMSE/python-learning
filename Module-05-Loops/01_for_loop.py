# Types of Loops in Python

# Python mainly has:

# for loop
# while loop


# 1. for Loop
# What is a for loop?

# A for loop is used when we know how many times we want to repeat something.

# Syntax:
# for variable in sequence:
#     code

# Example 1:

for i in range(5):
    print("Siddharth")


# Example 2
# Print numbers from 1 to 5

for i in range(1, 6):
    print(i)

# Example 4
# Print squares

for i in range(1, 6):
    print(i * i)


# Example 5
# Print even numbers

for i in range(2, 11, 2):
    print(i)